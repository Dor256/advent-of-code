import { describe, it, expect } from "bun:test";
import { solve } from ".";
  

describe("Part1", () => {
  it("test1", async () => {
    const input = `.......S.......
...............
.......^.......
...............
......^.^......
...............
.....^.^.^.....
...............
....^.^...^....
...............
...^.^...^.^...
...............
..^...^.....^..
...............
.^.^.^.^.^...^.
...............`;
    const {p1} = await solve(input);
    expect(p1()).toEqual(21);
  });

  it("solves", async () => {
    const {p1} = await solve();
    expect(p1()).toEqual(1555);
  });
});

